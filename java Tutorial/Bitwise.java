/* Bitwise Operator */

class Bitwise {
    public static void main(String[] args) {
        int a = 7;
        int b = 14;

        System.out.println("AND " + (a & b));
        System.out.println("OR " + (a | b));
        System.out.println("XOR " + (a ^ b));
        System.out.println("Comliments " + (~a));

    }

}