class ForLoop {
    public static void main(String[] args) {

        // Declare and initialize an array named 'arr'
        int arr[] = { 10, 20, 30, 40, 50 };

        // Use an enhanced for loop to iterate through the elements of the array
        // 'get' represents the current element in each iteration
        for (int get : arr) {
            // Print the current element
            System.out.println(get);
        }
    }
}
